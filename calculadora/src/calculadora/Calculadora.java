package calculadora;

public class Calculadora {
	public double soma(double x, double y) {
		return x + y;
	}
	public double subtracao(double x, double y) {
		return x - y;
	}
	public double multiplicar(double x, double y) {
		return x * y;
	}
	public double dividir(double x, double y) {
		return x / y;
	}
}
